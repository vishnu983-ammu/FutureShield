"use strict";

const fs = require("fs");
const os = require("os");
const path = require("path");

function sdkMarkerExists(sdkDir) {
  return fs.existsSync(path.join(sdkDir, "platform-tools"));
}

function findAndroidSdk() {
  const candidates = [
    process.env.ANDROID_HOME,
    process.env.ANDROID_SDK_ROOT,
    process.env.LOCALAPPDATA && path.join(process.env.LOCALAPPDATA, "Android", "Sdk"),
    path.join(os.homedir(), "AppData", "Local", "Android", "Sdk"),
    path.join(os.homedir(), "Android", "Sdk"),
    process.platform === "darwin" && path.join(os.homedir(), "Library", "Android", "sdk"),
    "/usr/local/lib/android/sdk",
    "/opt/android-sdk",
  ].filter(Boolean);

  for (const dir of candidates) {
    const resolved = path.resolve(dir);
    if (sdkMarkerExists(resolved)) return resolved;
  }
  return null;
}

/**
 * Write mobile/android/local.properties with sdk.dir (gitignored).
 * Sets ANDROID_HOME on process.env when found.
 * @param {string} androidDir - path to mobile/android
 * @returns {string} resolved SDK path
 */
function ensureAndroidSdk(androidDir) {
  const sdkDir = findAndroidSdk();
  if (!sdkDir) {
    console.error("\nERROR: Android SDK not found.");
    console.error("Install Android Studio or the command-line SDK, then either:");
    console.error("  1. Set ANDROID_HOME to your SDK folder (e.g. C:\\Users\\YOU\\AppData\\Local\\Android\\Sdk)");
    console.error("  2. Create mobile/android/local.properties with: sdk.dir=C\\:\\\\Users\\\\YOU\\\\AppData\\\\Local\\\\Android\\\\Sdk");
    process.exit(1);
  }

  process.env.ANDROID_HOME = sdkDir;
  process.env.ANDROID_SDK_ROOT = sdkDir;

  const propsPath = path.join(androidDir, "local.properties");
  const sdkDirForProps = sdkDir.replace(/\\/g, "/");
  const content = `## Auto-generated by FutureShield build scripts — do not commit\nsdk.dir=${sdkDirForProps}\n`;

  const existing = fs.existsSync(propsPath) ? fs.readFileSync(propsPath, "utf8") : "";
  if (!existing.includes(sdkDirForProps)) {
    fs.writeFileSync(propsPath, content, "utf8");
    console.log(`✓ Android SDK: ${sdkDir}`);
    console.log(`✓ Wrote ${propsPath}`);
  }

  return sdkDir;
}

module.exports = { findAndroidSdk, ensureAndroidSdk };
